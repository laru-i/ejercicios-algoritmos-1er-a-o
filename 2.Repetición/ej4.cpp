#include <iostream>

using namespace std;

/*
Informar la cantidad de números negativos que hay dentro de 20 números ingresados por teclado.
*/

int main()
{
    int num;
      
    for (int i = 1; i <= num * 2; i++)
    {
      cout << "Ingrese 20 numeros ";
      cin >> num;
    }
       
    return 0;
}